export function getAllBreeds(): Breed[] {
  return allBreeds;
}

export function getBreedBySlug(slug: string): Breed | null {
  return allBreeds.find(breed => breed.slug === slug) || null;
}

export function getBreedById(id: string): Breed | null {
  return allBreeds.find(breed => breed.id === id) || null;
}

export function getBreedByCategory(categoria: string): Breed[] {
  return allBreeds.filter(breed => breed.categoria === categoria);
}

export function searchBreeds(query: string): Breed[] {
  const lowerQuery = query.toLowerCase();
  return allBreeds.filter(breed =>
    breed.nome.toLowerCase().includes(lowerQuery) ||
    breed.slug.toLowerCase().includes(lowerQuery) ||
    breed.nome_en?.toLowerCase().includes(lowerQuery) ||
    breed.apelidos?.some((apelido: string) => apelido.toLowerCase().includes(lowerQuery))
  );
}

export function getPopularBreeds(limit?: number): Breed[] {
  const sorted = [...allBreeds].sort((a, b) => (b.popularidade || 0) - (a.popularidade || 0));
  return limit ? sorted.slice(0, limit) : sorted;
}

export function getBreedsByCategory(categoria: string): Breed[] {
  return getBreedByCategory(categoria);
}
